// Comparison or Relational operator

public class tut9 {
    public static void main(String[] args) {
        int a = 9;
        int b = 8;

        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(!(a == b));
        System.out.println(!(a > b));
    }
}
